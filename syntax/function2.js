function sum(four, five){ // w정의된 함수의 매개변수(parameter)

  return (four + five);
}

console.log(sum(4, 5));//호출되는 함수의 전달인자(argument)
//매개변수는 함수의 정의부분에 나열되어 있는 변수들을 의미하며, 전달인자는 함수를 호출할때 전달되는 실제 값을 의미한다.
//이같은 의미를 명확히 하기 위해 매개변수는 변수(parameter)로, 전달인자는 값(value)으로 보는것이 일반적이다.
//아직은 이해하기 힘들다!
